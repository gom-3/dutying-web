type ShiftDate = {
  day: string[];
  evening: string[];
  night: string[];
};

const dutyByDate: ShiftDate[] = [];
const temp = [
  ['오종욱', '김찬규', '조성연', '황영희'],
  ['황인서', '김범진', '류원경', '김은숙'],
  ['강명구', '안재홍', '김범진', '정경화'],
];

for (let i = 1; i <= 30; i++) {
  dutyByDate.push({
    day: temp[i % 3],
    evening: temp[(i + 1) % 3],
    night: temp[(i + 2) % 3],
  });
}

type DutyMonth = {
  month: number;
  shift: ShiftDate[];
};

const dutyByMonth: DutyMonth[] = [];
for (let i = 1; i <= 12; i++) dutyByMonth.push({ month: i, shift: dutyByDate });
export { dutyByDate, dutyByMonth };
